public class packages {
    int dateFrom;
    int dateTo;
    String city;
    double cost;
    String hotelName;
    String description;

    public packages(int dateFrom, int dateTo, String city, double cost, String hotelName, String description) {
        this.dateFrom = dateFrom;
        this.dateTo = dateTo;
        this.city = city;
        this.cost = cost;
        this.hotelName = hotelName;
        this.description = description;
    }



}
